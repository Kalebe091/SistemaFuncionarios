    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        jButton3.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String nome = pesquisaNome.getText();
                Departamento dept = BaseDadosDeparta.getInstancia().getDepartamentoPorNome(nome);
                if (dept != null) {
                    dadoDeparta.setText(dept.toString());  // Supondo que você tenha um método toString em Departamento que retorna uma representação em string do departamento
                } else {
                    dadoDeparta.setText("Departamento não encontrado");
                }
            }
        });

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

        // Limpa o modelo da tabela
        model.setRowCount(0);

        for (Funcionario func : BaseDadosFuncionario.getInstancia().getBaseFuncionarios()) {
            model.addRow(new Object[]{func.getNome(), func.getSalario()});
        }

    }//GEN-LAST:event_jTabbedPane1StateChanged

