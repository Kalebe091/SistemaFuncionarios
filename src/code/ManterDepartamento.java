    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (campoCodigo.getText().isEmpty() || campoNome.getText().isEmpty() || campoRamal.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos.", "Campos inválidos", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Departamento novoDept = new Departamento(
                Integer.parseInt(campoCodigo.getText()), campoNome.getText(),
                campoRamal.getText());
        base.setBaseDepatamentos(novoDept);
        JOptionPane.showMessageDialog(this, "Departamento cadastrado com Sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        //Atualiza JComboBox
        jComboBox1.setModel(new DefaultComboBoxModel(base.getNomes()));
        jComboBox1.updateUI();
        //Atualiza JTable
        deptModeloTabela.atualizaLista(BaseDadosDeparta.getBaseDepatamentos());
        jTable1.setModel(deptModeloTabela);
        jTable1.updateUI();

        campoCodigo.setText("");
        campoNome.setText("");
        campoRamal.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        jComboBox1.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent event) {
                JComboBox comboBox = (JComboBox) event.getSource();
                String nomeFuncionarioSelecionado = (String) comboBox.getSelectedItem();
                Funcionario funcionarioSelecionado = null;

                for (Funcionario funcionario : BaseDadosFuncionario.getInstancia().getBaseFuncionarios()) {
                    if (funcionario.getNome().equals(nomeFuncionarioSelecionado)) {
                        funcionarioSelecionado = funcionario;
                        break;
                    }
                }

                if (funcionarioSelecionado != null) {
                    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                    model.setRowCount(0); // Limpa o modelo da tabela
                    model.addRow(new Object[]{funcionarioSelecionado.getCargaHoraria(), funcionarioSelecionado.getSalario()});
                }
            }
        });


    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {
                  String nomeDepartamentoSelecionado = (String) jComboBox1.getSelectedItem();
                  Departamento departamentoSelecionado = null;

                  for (Departamento departamento : baseDepatamentos) {
                      if (departamento.getNome().equals(nomeDepartamentoSelecionado)) {
                          departamentoSelecionado = departamento;
                          break;
                      }
                  }

                  if (departamentoSelecionado != null) {
                      List<Funcionario> funcionariosDoDepartamento = BaseDadosFuncionario.getInstancia().getFuncionariosPorDepartamento(departamentoSelecionado.getNome());
                      for (Funcionario funcionario : funcionariosDoDepartamento) {
                          System.out.println("Nome: " + funcionario.getNome() + ", Salário: " + funcionario.getSalario());
                      }
                  }
              }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

