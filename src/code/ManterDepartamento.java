    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:
        try {
            int codigo = Integer.parseInt(campoCod.getText());
            String nome = campoNomeAlterar.getText();
            String ramal = campoRamalAlterar.getText();

            Departamento deptExistente = BaseDadosDeparta.getInstancia().getDepartamentoPorCodigo(codigo);
            if (deptExistente != null) {
                deptExistente.setNome(nome);
                deptExistente.setRamal(ramal);
                BaseDadosDeparta.getInstancia().alterarDepartamento(deptExistente);

                JOptionPane.showMessageDialog(this, "Departamento alterado com Sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                //Atualiza JComboBox
                jComboBox1.setModel(new DefaultComboBoxModel(base.getNomes()));
                jComboBox1.updateUI();
                //Atualiza JTable
                deptModeloTabela.atualizaLista(BaseDadosDeparta.getBaseDepatamentos());
                jTable1.setModel(deptModeloTabela);
                jTable1.updateUI();
                campoCod.setText("");
                campoNomeAlterar.setText("");
                campoRamalAlterar.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Departamento não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

